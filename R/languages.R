#' CA-BAT languages
#'
#' Lists the languages available for CA-BAT implementations.
#' @export
cabat_languages <- function() {
  c(
    English = "en",
    French = "fr",
    German = "de",
    `German (Formal)` = "de_f",
    Italian = "it",
    Spanish = "es",
    Russian = "ru",
    Dutch = "nl"
  )
}
